export function getProductBySlug(slug: string): Product | undefined {
  return products.find((p) => p.slug === slug);
}

export function getProductById(id: string): Product | undefined {
  return products.find((p) => p.product_id === id);
}

export function getProductsByCategory(categorySlug: string): Product[] {
  return products.filter((p) => p.category_slug === categorySlug && p.is_active);
}

export function getFeaturedProducts(): Product[] {
  return products.filter((p) => p.is_featured && p.is_active).sort((a, b) => a.priority_rank - b.priority_rank);
}

export function getHomepageProducts(): Product[] {
  return products.filter((p) => p.show_on_homepage && p.is_active).sort((a, b) => a.priority_rank - b.priority_rank);
}

export function getTrendingProducts(): Product[] {
  return products.filter((p) => p.show_in_trending && p.is_active);
}
